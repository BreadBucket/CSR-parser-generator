#pragma once


template <typename T>
struct interval {
// -------------------------------- //
public:
	T start;
	T end;
	
// -------------------------------- //
public:
	inline T length();
	
// -------------------------------- //
};




// ----------------------------------- [ Functions ] ---------------------------------------- //


template <typename T>
T interval<T>::length(){
	return end - start;
}


